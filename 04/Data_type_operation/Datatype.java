public class Datatype {
	public static void main(String[] args) {
		System.out.println(6); // Number
		System.out.println("6"); // String
		
		System.out.println(6+6); // 12
		System.out.println("6" + "6"); // 66 문자열의 덧셈은 두 문자를 이어붙임
		
		System.out.println(6*6); // 36
		// System.out.println("6" * "6"); 문자열은 곱셈이 불가능
		
		System.out.println("1111".length()); // 문자열의 길이를 구함
		// System.out.println(1111.length()); 숫자의 길이를 구하는 연산은 없음
	}
}